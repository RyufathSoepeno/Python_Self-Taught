#Tuple and Sets in Python


#Tuple is a list which you can't change or add the values, but can use index
#Round brackets are tuple

a = (21, 36, 12, 25)
print(a)

#Set is a collection of unique elements
#You cannot define/Index or change a set 'b(1)', but you can add items
#Sets are "hash" we cannot tell the sequence
#Curly brackets are sets

b = {72, 45, 5, 78}
print(b)

c = {25, 14, 98, 63, 75, 98}
print(c)

#It is possible for both sets and tuples to be added, index, or changed
